abstract class stats_data_controller {
    /** @var stats_data access stats data */
    protected $stats_data;

    /**
     * Execution of various processes.
     */
    public function run() {
        $this->stats_data->init_access_numbers();
        $this->stats_data->analyze_access_records();
        $this->stats_data->align_access_numbers_by_range();
    }
}
